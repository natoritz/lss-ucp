		<center><b><font size="6">LSS-RP Kuralları</font></b></center>

<br>Sunucudaki huzuru ve adaleti sağlamak için LSS-RP'nin oyun, <a href="https://forum.lss-roleplay.com" target="_blank">forumlar</a> ve Kullanıcı Kontrol Paneli (bu web sitesi) için bir dizi kuralı vardır. Tüm oyuncuların sunucuda oynamadan önce kuralları okuması ve onlara uyması gerekir. LSS-RP kuralları sık sık gözden geçirilir ve personel ile topluluk arasındaki uzun istişarelerin bir sonucudur, bu nedenle kuralları sürekli güncel tutuyoruz.
<br>
<br><b>UCP kuralları:</b>
<br>&raquo; <b>1)</b> Karakter başvurusu yaparken tüm soruları kendiniz cevaplayın ve hiçbir şeyi kopyalayıp yapıştırmayın.
<br>&raquo; <b>2)</b> LSS-RP personelinden başvurunuzu incelemesini istemeyin.
<br>&raquo; <b>3)</b> Karakterlerinizden biri yasaklandıysa, yeni bir karakter oluşturmanıza veya farklı bir karakter kullanmanıza izin verilmez.
<br>&raquo; <b>4)</b> Diğer oyuncuların karakter veya oluşum tanıtım konularına spam/flame/troll mesajları göndermeniz yasaktır.
<br>&raquo; <b>5)</b> Size ait olmayan karakterleri hesabınıza aktarmayın.
